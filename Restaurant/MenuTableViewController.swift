    func updateUI(with menuItems: [MenuItem]) {
        DispatchQueue.main.async {
            self.menuItems = menuItems
            self.tableView.reloadData()
        }
    }

    // MARK: - Table view data source



    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return menuItems.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "menuItemCellIdentifier", for: indexPath)

        configure(cell, forItem: indexPath)

        return cell
    }
    
    func configure(_ cell: UITableViewCell, forItem indexPath: IndexPath) {
        let menuCellText = menuItems[indexPath.row]
        cell.textLabel?.text = menuCellText.name
        cell.detailTextLabel?.text = " € \(menuCellText.price)"
        MenuController.shared.fetchImage(url: menuCellText.imageURL) { (image) in
            guard let image = image else { return }
            DispatchQueue.main.async {
                if let currentIndexPath = self.tableView.indexPath(for: cell),
                    currentIndexPath != indexPath {
                    return
                }
                cell.imageView?.image = image
                cell.setNeedsLayout()
            }
        }
    }

